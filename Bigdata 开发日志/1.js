function get_data(obj) {
    var year_data = obj.year_data

    // 获取所有年份
    var years = Object.keys(year_data[0].year)
    // console.log(years);

    // 获取所有名字
    var names = []
    year_data.forEach(element => {
        names.push(element.name)
    })

    // 构造数组
    var results = {}
    years.forEach(year => {
        results[year] = []
        results[year + 'max'] = 0
        results[year + 'sum'] = 0
    })
    // console.log(results);

    // 对每个区进行遍历
    for (let index = 0; index < year_data.length; index++) {
        var one_year = year_data[index]
        var name_ = one_year.name
        var total_count = one_year.total_count
        var this_year = one_year.year

        // console.log(one_year);
        // one_year.year.forEach(element => {
        // })
        var max_count = 0
        if (name_ != '天津市') {
            years.forEach(year => {
                results[year].push({ "name": name_.replace('天津市', ''), "value": this_year[year] })


                // 最大值, 永远是天津市的值, 应该把天津市和各区分开
                results[year + 'max'] = this_year[year] > results[year + 'max'] ? this_year[year] : results[year + 'max']

                // 总计, 累加
                results[year + 'sum'] += this_year[year]
            })
        }
    }
    return results
}

